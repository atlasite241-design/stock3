export type TKey = keyof typeof DICT

function apply(lang: Lang) {
  document.documentElement.setAttribute('lang', lang === 'ar' ? 'ar' : 'fr')
  document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr')
}

interface LanguageCtx {
  lang: Lang
  toggleLang: () => void
  t: (key: TKey) => string
}

const Ctx = createContext<LanguageCtx | null>(null)

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLang] = useState<Lang>('fr')

  useEffect(() => {
    const saved = localStorage.getItem(KEY) as Lang | null
    const initial = saved ?? 'fr'
    setLang(initial)
    apply(initial)
  }, [])

  const toggleLang = useCallback(() => {
    setLang((l) => {
      const next: Lang = l === 'ar' ? 'fr' : 'ar'
      localStorage.setItem(KEY, next)
      apply(next)
      return next
    })
  }, [])

  const t = useCallback((key: TKey) => DICT[key][lang], [lang])

  return <Ctx.Provider value={{ lang, toggleLang, t }}>{children}</Ctx.Provider>
}

export function useLanguage() {
  const ctx = useContext(Ctx)
  if (!ctx) throw new Error('useLanguage must be used within LanguageProvider')
  return ctx
}
